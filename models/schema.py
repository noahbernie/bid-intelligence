    prebid_meeting_location: Optional[str] = None
    bond_required: Optional[bool] = None
    bond_pct: Optional[float] = None
    liquidated_damages_per_day: Optional[float] = None
    addenda_count: Optional[int] = None
    bid_platform: Optional[str] = None            # "planetbids"
    bid_submission_method: Optional[str] = None
    prevailing_wage_required: Optional[bool] = None
    parsed_fields_json: Optional[dict] = None     # catch-all for extra fields
