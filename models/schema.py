"""
Pydantic models mirroring the Supabase schema.
Each model maps 1:1 to a table. Field names match column names exactly.
"""

from __future__ import annotations
from datetime import datetime
from typing import Optional
from pydantic import BaseModel, field_validator


# ---------------------------------------------------------------------------
# Agency — the municipality / public body that posted the job
# ---------------------------------------------------------------------------
class Agency(BaseModel):
    id: Optional[str] = None          # UUID, generated by Supabase
    name: str
    type: Optional[str] = None        # e.g. "city", "county", "district"
    website: Optional[str] = None
    contact_email: Optional[str] = None
    contact_phone: Optional[str] = None
    location_city: Optional[str] = None
    location_state: Optional[str] = None
    external_portal_id: Optional[str] = None   # PlanetBids portal ID e.g. "17950"
    scrape_log_id: Optional[str] = None


# ---------------------------------------------------------------------------
# Source — the platform we scraped from (e.g. PlanetBids portal 17950)
# ---------------------------------------------------------------------------
class Source(BaseModel):
    id: Optional[str] = None
    name: str
    base_url: str
    scrape_frequency: Optional[str] = None
    last_scraped_at: Optional[datetime] = None
    is_active: bool = True
    auth_credentials_ref: Optional[str] = None


# ---------------------------------------------------------------------------
# Job — the bid opportunity posted by an agency
# (PlanetBids calls this a "bid"; we call it a "job")
# ---------------------------------------------------------------------------
class Job(BaseModel):
    id: Optional[str] = None
    job_owner: Optional[str] = None       # FK → agencies.id
    title: str
    description: Optional[str] = None
    scope_of_work: Optional[str] = None
    trade_category: Optional[str] = None
    project_type: Optional[str] = None    # e.g. "construction", "services"
    estimated_value: Optional[float] = None
    posted_date: Optional[datetime] = None
    bid_due_date: Optional[datetime] = None
    project_start_date: Optional[datetime] = None
    location_city: Optional[str] = None
    location_state: Optional[str] = None
    location_lat: Optional[float] = None
    location_lng: Optional[float] = None
    status: Optional[str] = None          # "open" | "closed" | "awarded" | "cancelled"
    source_url: Optional[str] = None
    source_platform: Optional[str] = None # "planetbids"
    invitation_num: Optional[str] = None  # PlanetBids invitation/bid number
    bid_type: Optional[str] = None        # "RFP" | "RFQ" | "Bid" | "RFI" etc.
    category_codes: Optional[str] = None  # comma-separated e.g. "70000, 96500"
    last_modified: Optional[datetime] = None
    scrape_log_id: Optional[str] = None

    @field_validator("status", mode="before")
    @classmethod
    def normalize_status(cls, v: str | None) -> str | None:
        if not v:
            return None
        mapping = {
            "bidding": "open",
            "open": "open",
            "closed": "closed",
            "awarded": "awarded",
            "cancelled": "cancelled",
            "canceled": "cancelled",
        }
        return mapping.get(v.lower(), v.lower())

    @field_validator("estimated_value", mode="before")
    @classmethod
    def parse_currency(cls, v):
        if not v:
            return None
        if isinstance(v, str):
            cleaned = v.replace("$", "").replace(",", "").strip()
            return float(cleaned) if cleaned else None
        return v


